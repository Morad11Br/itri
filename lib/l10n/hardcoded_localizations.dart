  BuildContext context,
  String english, [
  Map<String, Object?> vars = const {},
]) {
  var text = context.t(english);
  for (final entry in vars.entries) {
    text = text.replaceAll('{${entry.key}}', entry.value?.toString() ?? '');
  }
  return text;
}
